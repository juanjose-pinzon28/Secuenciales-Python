n1= float(input("Ingrese un numero: "))
import math

seno = math.sin(n1)
coseno = math.cos(n1)
tangente = math.tan(n1)
raiz = math.sqrt(n1)
logaritmoNatural = math.log(n1)

print("Del numero ",n1)
print("Su seno es:",seno)
print("Su coseno es:",coseno)
print("Su tangente es:",tangente)
print("Su raiz es:",raiz)
print("Su logaritmo natural es:",logaritmoNatural)
